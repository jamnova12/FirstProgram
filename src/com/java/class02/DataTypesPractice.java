package com.java.class02;

public class DataTypesPractice{

    public static void main(String str[]){
            byte num1=32;
            short num2=54;
            int num3=10;
            long num4=33;
            // Float needs a f at the end of the number so the software knows it's a float
            float num5=5.6f;
            double num6=48.09;
            //String is for double quotations
            String num7 = "This is my Java program!!" ;
            //Char is only for 1 character
            char num8='a';
            // Boolean is for True or False Only
            boolean num9=true;

            System.out.println(num1);
            System.out.println(num2);
            System.out.println(num3);
            System.out.println(num4);
            System.out.println(num5);
            System.out.println(num6);
            System.out.println(num7);
            System.out.println(num8);
            System.out.println(num9);
    }
}
